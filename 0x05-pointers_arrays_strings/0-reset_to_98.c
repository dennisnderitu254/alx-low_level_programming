#include "main.h"
/**
 *reset_to_98 - reset value of an integer to 98
 *@n: parameter targeted
 *Return : nothing
 */

/*
1. It’s declaring a function called reset_to_98 that takes an int pointer as a parameter.
2. It’s setting the value of the int pointed to by n to 98.
*/
/*
Time Complexity: O(1)
*/
void reset_to_98(int *n)
{
	*n = 98;
}
